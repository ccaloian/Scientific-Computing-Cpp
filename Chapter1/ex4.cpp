#include <iostream>

int main(int argc, char* argv[])
{
    double A[2][2] = {{1, 2}, {3, 4}};
    double B[2][2] = {{5, 6}, {7, 8}};

    // compute C=A+B
    double C[2][2];
    C[0][0] = A[0][0] + B[0][0]; 
    C[0][1] = A[0][1] + B[0][1]; 
    C[1][0] = A[1][0] + B[1][0]; 
    C[1][1] = A[1][1] + B[1][1]; 
    std::cout << "C=A+B\n";
    std::cout << C[0][0] << "\t" << C[0][1] << "\n" << C[1][0] << "\t" << C[1][1] << "\n\n";

    // compute D=AB
    double D[2][2];
    D[0][0] = (A[0][0] * B[0][0]) + (A[0][1] * B[1][0]); 
    D[0][1] = (A[0][0] * B[0][1]) + (A[0][1] * B[1][1]); 
    D[1][0] = (A[1][0] * B[0][0]) + (A[1][1] * B[1][0]); 
    D[1][1] = (A[1][0] * B[0][1]) + (A[1][1] * B[1][1]); 
    std::cout << "D=AB\n";
    std::cout << D[0][0] << "\t" << D[0][1] << "\n" << D[1][0] << "\t" << D[1][1] << "\n\n";
    
    return 0;
}
